const axios = require('axios');
const cheerio = require('cheerio');

async function styleText(teks, api = `http://qaz.wtf/u/convert.cgi?text=${encodeURIComponent(teks)}`) {
  try {
    const { data } = await axios.get(api, {
      headers: { 'User-Agent': 'Mozilla/5.0' }
    });

    const $ = cheerio.load(data);
    const hasil = $('table > tbody > tr').map((_, row) => ({
      name: $(row).find('td:nth-child(1) > span').text(),
      result: $(row).find('td:nth-child(2)').text().trim()
    })).get(); 

    return hasil;
  } catch (error) {
    console.error('Error fetching styled text:', error.message);
    throw new Error('Gagal mengambil data dari API. Pastikan API tersedia.');
  }
}

function convertText(input, charMap) {
  return String(input).split('').map(char => charMap[char] || char).join('');
}

